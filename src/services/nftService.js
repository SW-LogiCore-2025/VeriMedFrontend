const alchemyUrl = 'https://eth-sepolia.g.alchemy.com/v2/cXhgKUcT4kq8Q9wUv4FiGGxTk0cSrlYM';

export const nftService = {
    async fetchNFTs() {
        try {
            const provider = new ethers.providers.JsonRpcProvider(alchemyUrl);
            const contract = new ethers.Contract(contractAddress, contractABI, provider);

            const nextTokenId = await contract.nextTokenId();
            const tokenPromises = [];

            for (let i = 1; i < nextTokenId; i++) {
                tokenPromises.push(
                    (async () => {
                        try {
                            const tokenURI = await contract.tokenURI(i);
                            const owner = await contract.ownerOf(i);
                            return { tokenId: i.toString(), tokenURI, owner };
                        } catch (error) {
                            if (error.code === ethers.errors.CALL_EXCEPTION) {
                                console.warn(`El token con ID ${i} no existe en el contrato.`);
                            } else {
                                console.error(`Error al obtener el token con ID ${i}:`, error);
                            }
                            return null; // Retorna null si hay un error
                        }
                    })()
                );
            }

            const tokens = (await Promise.all(tokenPromises)).filter(Boolean); // Filtra los valores null
            console.log('NFTs obtenidos:', tokens);
            return tokens;
        } catch (error) {
            console.error('Error al obtener los NFTs:', error);
            return [];
        }
    },
};